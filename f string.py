'''
f‐string allows us to execute code inside of a string…right there between the
brackets:
{....}
fʺstring stuff here {code stuff here}ʺ
'''
print(f"3 + 2 = {3 + 2}")
print(f"5 – 1 = {5 - 1}")
print(f"3 * 4 = {3 * 4}")
print(f"10 / 5 = {10 / 5}")
print(f"35 % 3 = {35 % 3}")
print(f"2 ** 4 = {2 ** 4}")